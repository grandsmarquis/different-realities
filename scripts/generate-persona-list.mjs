/**
 * Reads src/personas/index.js for persona order (export const personas = [...])
 * and each ./<id>.js for id, label, emoji, description — writes src/personas/personaList.js
 * Run after adding or reordering personas: node scripts/generate-persona-list.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { leet733Str } from '../src/utils/leet733.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const personasDir = path.join(root, 'src/personas')
const indexPath = path.join(personasDir, 'index.js')
const listPath = path.join(personasDir, 'personaList.js')
const outPath = listPath

function parseStringField(src, field) {
  const idx = new RegExp(`\\b${field}:\\s*`).exec(src)
  if (!idx) return null
  let i = idx.index + idx[0].length
  const q = src[i]
  if (q !== "'" && q !== '"' && q !== '`') return null
  i++
  let out = ''
  while (i < src.length) {
    const c = src[i]
    if (c === '\\') {
      out += src[i + 1] ?? ''
      i += 2
      continue
    }
    if (c === q) break
    out += c
    i++
  }
  return out
}

function extractPersonaOrderFromIndex(indexSrc) {
  const m = indexSrc.match(/export const personas = \[([\s\S]*?)\]\s*\n\s*export const personaMap/)
  if (!m) return null
  return m[1]
    .split(',')
    .map((s) => s.trim())
    .filter((t) => /^[a-zA-Z_$][\w$]*$/.test(t))
}

function extractLoadKeyOrderFromPersonaList(listSrc) {
  const keys = [...listSrc.matchAll(/loadKey:\s*"([^"]+)"/g)].map((x) => x[1])
  return keys.length > 0 ? keys : null
}

function resolveOrder() {
  if (fs.existsSync(listPath)) {
    const fromList = extractLoadKeyOrderFromPersonaList(fs.readFileSync(listPath, 'utf8'))
    if (fromList) return fromList
  }
  if (fs.existsSync(indexPath)) {
    const fromIndex = extractPersonaOrderFromIndex(fs.readFileSync(indexPath, 'utf8'))
    if (fromIndex) return fromIndex
  }
  throw new Error(
    'Cannot resolve persona order: need src/personas/personaList.js with loadKey fields or legacy src/personas/index.js with export const personas = [...]',
  )
}

function main() {
  const order = resolveOrder()
  const rows = []

  for (const loadKey of order) {
    const filePath = path.join(personasDir, `${loadKey}.js`)
    if (!fs.existsSync(filePath)) {
      throw new Error(`Missing persona file for ${loadKey}: ${filePath}`)
    }
    if (loadKey === 'rightIn733tc0d3') {
      rows.push({
        id: 'right-in-733tc0d3',
        loadKey,
        label: leet733Str('Right in 733tc0d3'),
        emoji: '🧡',
        description: leet733Str(
          'Competitive-coding grind: problems as inbox, judge cluster weather, discuss news, benchmark tickers.',
        ),
      })
      continue
    }
    const src = fs.readFileSync(filePath, 'utf8')
    const id = parseStringField(src, 'id')
    const label = parseStringField(src, 'label')
    const emoji = parseStringField(src, 'emoji')
    const description = parseStringField(src, 'description') ?? ''
    if (!id) {
      throw new Error(`${loadKey}.js: missing id field`)
    }
    if (label == null || emoji == null) {
      throw new Error(`${loadKey}.js: missing label or emoji`)
    }
    rows.push({ id, loadKey, label, emoji, description })
  }

  const json = rows
    .map(
      (r) =>
        `  { id: ${JSON.stringify(r.id)}, loadKey: ${JSON.stringify(r.loadKey)}, label: ${JSON.stringify(r.label)}, emoji: ${JSON.stringify(r.emoji)}, description: ${JSON.stringify(r.description)} }`,
    )
    .join(',\n')

  const banner = `/**\n * Generated by scripts/generate-persona-list.mjs — do not edit by hand.\n */\n\n`
  const body = `${banner}/**\n * Lightweight persona metadata for the selector (no layout imports).\n */\nexport const personas = [\n${json},\n]\n\nexport const personaMap = Object.fromEntries(personas.map((p) => [p.id, p]))\n`

  fs.writeFileSync(outPath, body, 'utf8')
  console.log(`Wrote ${rows.length} personas to ${path.relative(root, outPath)}`)
}

main()
